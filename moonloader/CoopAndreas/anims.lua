local lastAName = {}
local lastALib = {}
function getCharCurrentAnimation(ped)
    if isCharPlayingAnim(ped, lastAName[ped]) then return {lastALib[ped], lastAName[ped]} end
    local alib = ''
    local aname = ''
    for key, value in pairs(anims) do
        if isCharPlayingAnim(ped, value) then 
            alib = libs[key]   
            aname = value
            break
        end
    end
    lastAName[ped] = aname
    lastALib[ped] =  alib
    return {alib, aname}
end
function getAnimationIdByName(name)
    local id = nil
    for key, value in pairs(anims) do
        if value == name then 
            id = key
            break
        end    
    end
    return id
end
function getAnimationById(id)
    return {libs[id], anims[id]}
end
function doesAnimLibExist(alib)
    for _, value in pairs(uniqueLibs) do
        if value == alib then 
            return true
        end    
    end
    return false
end